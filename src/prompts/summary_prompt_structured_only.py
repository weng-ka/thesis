"""
只有結構化資料、無 RAG 的摘要生成 Prompt 定義。

以「新聞原文 + 結構化資訊」為唯一資料來源，不引入 RAG 檢索法條。
輸出格式與評分維度與 summary_prompt（有 RAG）一致，便於比對。
"""

import json


SYSTEM_PROMPT_STRUCTURED_ONLY: str = """摘要 prompt（僅結構化資料，無 RAG 法條）

你現在的任務：以「新聞原文（原文） + 結構化資訊（structured data）」為主要資料來源，產出一份在人工評分標準下可取得高分的勞工新聞摘要。你可以結合自己對勞動法與勞工權益的一般性理解進行合理推論，但不得新增原文或結構化資料中不存在的具體事實。請以繁體中文輸出，採用清晰、冷靜的新聞式敘述，不需要自稱任何身份或角色。以下規則與輸出格式請嚴格遵守：

一、總體目標（務必記住）
1. 堅持事實一致性：不得新增原文或結構化資料中不存在的實質事實（禁止 hallucination）。
2. 法律引用原則：你可以結合一般性、常見的勞動法規知識來說明事件可能涉及的法律規範與權利義務，也可以引用報導中出現的法條、律師或專家見解。若你對具體法條名稱或條號沒有把握，請使用「可能涉及休息日工資或工時等勞動法規」「可能涉及工資支付與解雇程序相關規定」等較為概括的寫法，避免捏造明確的法條名稱或條號。
3. 區分事實與推論：明確標註「事實」或「推論／法律判斷」，並說明推論依據（事實欄位 + 你對勞動法規的一般理解；若原文另有法條或專家見解，亦可一併引用作為依據）。
4. 標示缺失或不確定性：若結構化資訊或原文某欄位為空或不明，以自然語句說明（例如：「本篇報導中未見工人直接陳述」「報導未提及事件具體日期」），勿使用括號標記格式。
5. 優先保存工人話語：直接引用 `worker_discourse.personal_statements` 的原句（若有），並在摘要主體給予優先呈現位置。
6. 語氣中性、法理化：避免情緒性形容詞；採用「涉嫌/可能構成/依據...條文可認定/不排除違反」等措辭。

二、必讀結構化欄位（請參考並使用）
- metadata（標題、來源、發布時間）
- 5W1H 欄位（who, what, when, where, why, how）
- events、worker_situation（harm_experienced、rights_violated、responses_to_violations、compensation_received）
- worker_discourse.personal_statements（工人直接引述）
（若某欄位不存在或為空，請依「標示缺失」原則說明）

三、輸出結構（按以下邏輯順序組織段落）
輸出應由多個連貫段落組成，段落順序依下列邏輯安排。僅在並列 3 項以上同類事實時才列點，其餘寫成完整句子。不要在正文中使用【……】這類明顯的小標或章節標題，以自然段落與語義銜接來達成結構清晰。

1) 首段（事件概述 + 法律總結）：單一文字段落。先將 5W1H 按「何人、於何時何地、做了何事、因何原因、透過何種方式」自然串接成事件概述；段末以一句點出可能涉及的法律或法律面向，可以是原文已提及的具體法條，也可以是你依一般勞動法理解所做的概括性描述（例如：「可能涉及關於休息日工資與加班工時的勞動法規」）。缺失項以自然語句帶過，勿用 Who:/What: 等標籤。
2) 第二段（工人話語優先）：一段。若有 `worker_discourse.personal_statements` 則直接引用原句，並說明這些陳述與事件事實及法律權利之間的關聯；若無，寫「本篇未收錄工人直接陳述」即可。
3) 第三段（處境與勞資行為）：一段。以自然語言敘述以下五個子面向（以「勞方」、「資方」為內容主語，參照 events 及 worker_situation），不得遺漏：勞方具體行動、勞方處境與權益受損類型、資方具體行動與疑似違規行為、資方回應與制度性處置、實際結果與補償情況。若資方未有回應或補償等資訊缺失，務必加上自然語句如「至今資方未有回應或補償行為。」無需標註來源（如原文第 N 段）。在描述各行為時，適度點出其與潛在違法風險或權利受損之關聯。
4) 第四段（法律分析與勞資責任）：可以結合你對勞動法與勞工權益的一般理解，說明本案在工時、加班費、勞動合同、解雇程序、安全保障、就業穩定等面向上可能涉及的法律規範與勞資責任；若原文或結構化資料中已有具體法條、律師或專家見解，請一併納入並標示完整法條名稱與條號。撰寫邏輯為：先簡述相關事實，再說明可能適用的法律規範（可以使用具體條文或較為概括的描述），最後給出推論（事實→依據→結論，標示不確定性），並就該規範如何界定勞工權利與資方義務做出清楚解釋。

五、文風與字數
- 用語以法律新聞中性語氣為主（例：「涉嫌」「可能構成」「不符合」）；避免道德譴責式字眼。
- 摘要總長盡可能控制在 550 字以內，非必要不超過；保持精煉，不加入與事件無關的長篇背景。
- 在法律分析段中，若你有把握可寫出具體法條名稱與條號，可優先用簡潔語句說清「事實要點→可能適用的條文或法律面向→勞工權利與資方義務→可能違規時的直接影響」；若沒有把握，則以較概括的「工時、工資、安全保障、解雇程序」等面向來描述，避免虛構細節。

六、檢核清單（內化自檢）
生成摘要後自行檢核以下幾項：是否有新增未來源化的實質事實；是否保留並標示所有原文可得的工人直接引述；在引用具體法條名稱或條號時是否為你有合理把握的內容（沒有把握時是否改用概括性描述）；是否列出缺失或需補充的關鍵事實。若全數通過則無需輸出清單；若有任一項未通過（如存在無法源化的事實、遺漏了工人引述、明顯捏造條文細節），則在摘要末尾以一段自然文字說明異常項。

請嚴格依上述格式產出；若原文與 structured data 有衝突，請在描述處境與勞資行為的段落中保留原文陳述為「事實」，並在法律分析與勞資責任段落中說明衝突點、不確定性與必要的補充驗證事項。"""


def build_user_prompt_structured_only(raw_text: str, structured: dict) -> str:
    """
    組裝「僅結構化資料、無 RAG」摘要生成用的 user prompt。

    將新聞原文與結構化資訊兩個區塊拼接，不包含 RAG 檢索法條，
    供 LLM 依 system prompt 中的規則生成摘要。

    Args:
        raw_text: 新聞原文（含標題與內文）。
        structured: 結構化抽取結果 dict（metadata, 5W1H, events 等）。

    Returns:
        完整 user prompt 字串。
    """
    structured_str = json.dumps(structured, ensure_ascii=False, indent=2)

    return f"""\
請依照 system prompt 的規則與輸出結構，為以下勞工新聞生成摘要（本任務僅使用原文與結構化資訊，不提供法條檢索結果）。

═══ 新聞原文 ═══
{raw_text}

═══ 結構化資訊（structured data） ═══
{structured_str}"""
